public class Argorithm<T, K>{

    private T answer;
    private K test_cases;

    public T getAnswer() {
        return answer;
    }
    public void setAnswer(T answer) {
        this.answer = answer;
    }
    public K getTest_cases() {
        return test_cases;
    }
    public void setTest_cases(K test_cases) {
        this.test_cases = test_cases;
    }

}
